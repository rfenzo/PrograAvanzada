def mayor(a, b):
    return a > b


def menori(a, b):
    return a <= b


def mayori(a, b):
    return a >= b


def distinto(a, b):
    return a != b


def igual(a, b):
    return a == b


def mas(a, b):
    return a+b


def menos(a, b):
    return a-b


def por(a, b):
    return a*b


def div(a, b):
    return a/b


def aprox(a, b):
    return round(a, b)
